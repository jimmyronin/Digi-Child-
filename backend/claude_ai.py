"""
Claude-powered child dialogue for Digi-Child.

Mira's spoken/acted responses are generated by the Claude API, grounded in the
developmental-psychology "library" below plus her live simulation state
(age, trust, volatility, temperament, consecutive mistreatments, setting).

This module is the "live" brain. If the Claude API is unavailable for any
reason -- no API key, the `anthropic` package isn't installed, a network or
API error -- callers (main.py) fall back to the rule-based engine in
`local_ai.py`. That fallback is the app's DEMO MODE: it always works offline,
so a live demo never breaks even without credentials.
"""

import os
