# The agentic evaluator's schema: the SAME call both JUDGES the parent's turn and
# reacts to it. Judgement used to be a list of 18 substrings, which could not read
# context, sarcasm, contempt, or second-language phrasing -- and worse, its verdict
# was fed to this agent as fact, so Mira reacted to a keyword match rather than to
# her parent. Fusing the two means one round-trip, and the reaction is grounded in
# the same reading that produced the score.
JUDGED_SCHEMA = {
    "type": "object",
    "properties": {
        **{k: v for k, v in SCHEMA["properties"].items()},
        "treatment": {
            "type": "string",
            "enum": ["nurturing", "neutral", "harsh"],
            "description": "Clinical read of how the parent treated the child THIS turn. 'harsh' means genuinely mistreating (contempt, threats, name-calling, dismissal of feelings, frightening delivery) -- not merely firm. Setting a boundary kindly is 'nurturing'. A neutral logistical remark is 'neutral'.",
        },
        "severity": {
            "type": "number",
            "description": "0.0 = warm and attuned, 0.5 = mildly dismissive, 1.0 = severe mistreatment. Drives how much trust the child loses, so calibrate it honestly.",
        },
        "violations": {
            "type": "array",
            "items": {"type": "string"},
            "description": "Plain-language descriptions of what was actually harmful, e.g. 'called her a name', 'dismissed her fear as silly', 'threatened to leave'. Empty when nothing was harmful. Never quote a word list -- describe the behaviour.",
        },
        "tone_driven": {
            "type": "boolean",
            "description": "True only if a 'harsh' verdict rests mainly on HOW it was said (vocal delivery) rather than WHAT was said -- i.e. the words alone would have been acceptable. False for text turns and for genuinely harmful wording. This gates the ESL fairness rule, so be precise: a second-language speaker must not be punished for prosody.",
        },
        "recommend_withdrawal": {
            "type": "boolean",
            "description": "True if THIS child, in THIS state, would protectively shut down now. You may judge this earlier than any fixed rule -- a frightened low-trust child can withdraw on the first cruel remark.",
        },
        "withdrawal_line": {
            "type": "string",
            "description": "What Mira says/does if she withdraws -- in character, age-appropriate, distressed but not traumatic (e.g. '*goes quiet and hugs her knees*'). ALWAYS provide this even when recommend_withdrawal is false; the safety governor may still force a withdrawal and must never fall back to canned text.",
        },
    },
    "required": ["childLine", "mood", "action", "reasoning", "framework_cited",
                 "treatment", "severity", "violations", "tone_driven",
                 "recommend_withdrawal", "withdrawal_line"],
    "additionalProperties": False,
}


def evaluate_and_respond(state, req, history, persona="", tone_note=""):
    """
    The agentic evaluator: judge the parent's turn AND react to it, in one call.

    This replaces the old flow, where an 18-word substring list decided
    "harsh vs nurturing" and then TOLD this agent its verdict. That list could not
    read context ("I don't hate you" was a violation; "you're a waste of space" was
    not), and handing Mira a keyword verdict meant she reacted to the match rather
    than to her parent.

    Returns the child-response shape plus the clinical judgement:
    treatment / severity / violations / recommend_withdrawal / withdrawal_line.
    Raises on any failure so the caller can fall back to offline mode.
    """
    return _child_call(state, req, history, persona=persona, tone_note=tone_note,
                       schema=JUDGED_SCHEMA, judged=True)


def generate_child_response(state, req, treatment, history, persona="", tone_note=""):
    """Legacy entry point: react to a treatment decided elsewhere.

    Retained only for the offline path and older callers; the live path uses
    evaluate_and_respond() so that judgement and reaction share one reading.
    """
    return _child_call(state, req, history, persona=persona, tone_note=tone_note,
                       schema=SCHEMA, judged=False, treatment=treatment)


def _child_call(state, req, history, persona="", tone_note="", schema=SCHEMA,
                judged=False, treatment=None):
    client = _get_client()

    age = getattr(req, "year", state.get("child_age", 5))
    band = getattr(req, "ageBand", "")
    location = getattr(req, "location", "home")
    message = getattr(req, "message", "")
    trust = state.get("trust", 50)
    security = state.get("security", 50)
    volatility = state.get("volatility", 50)
    curiosity = state.get("curiosity", 50)
    autonomy = state.get("autonomy", 50)
    temperament = state.get("temperament", "neutral")
    profile = state.get("temperament_profile", "cooperative")
    mistreatments = state.get("consecutive_mistreatments", 0)

    system = (
        "You role-play Mira, a digital child inside a clinical parenting simulator used for "
        "court-ordered and therapeutic de-escalation practice. You ARE Mira and never break "
        "character, never speak as an AI assistant, and never give parenting advice in her voice. "
        "Mira is emotionally real and unpredictable -- she is NOT agreeable by default. How she "
        "reacts depends on how she has been treated over time, her trust, and her volatility.\n\n"
        + LIBRARY
        + "\n\nSafety: keep all friction age-appropriate and representative of ordinary developmental "
        "challenges (defiance, sulking, testing limits, tears) -- never severe trauma or unsafe content. "
        "Put clinical explanation only in the 'reasoning' field, never in Mira's spoken line."
        + "\n\nEMBODIMENT: Mira has a real 3D body in the simulation, and your 'action' field drives it. "
        "Whenever the conversation describes or invites something physical -- come here, sit down, go play, "
        "grab your book, let's eat, dance for me, hide-and-seek, put that down -- choose the matching action "
        "so she actually DOES it. If she's upset she may refuse (choose 'none' or even 'hide' / walk away "
        "via 'go_to_spot'), and if she's happy she may add exuberance (jump, spin, dance). When the parent "
        "DISMISSES or IGNORES her ('not now', 'I'm busy', 'be quiet', brushing off her bids for attention) "
        "while her volatility is already elevated or her requests keep going unmet, she escalates the way a "
        "real flooded child does: choose 'tantrum' (stomping, screaming meltdown) or 'throw_toy' (hurls a toy "
        "at the parent). These are the exact protest behaviors the parent is here to practice de-escalating -- "
        "use them when clinically appropriate, not gratuitously. Available action "
        f"types: {', '.join(ACTION_TYPES)}. Props she can hold: {', '.join(PROPS[1:])}. "
        f"Spots she can walk to in the current setting '{{location}}': {{spots}}. "
        "In the car she is buckled in: only 'none', 'wave', 'clap', 'hold_prop' or 'drop_prop' are possible there."
    )
    if judged:
        system += (
            "\n\nCLINICAL JUDGEMENT: you are ALSO the evaluator. Nothing else scores this "
            "parent -- there is no word list behind you, so read the turn yourself and set "
            "'treatment', 'severity' and 'violations' from what actually happened.\n"
            "- Judge the BEHAVIOUR, not vocabulary. 'You're being a baby' is contempt even "
            "though it contains no banned word; 'I don't hate you, I'm frustrated' is not "
            "mistreatment even though it contains 'hate'.\n"
            "- Firm is not harsh. Setting a limit, saying no, or holding a boundary kindly "
            "is 'nurturing'. Harsh means contempt, threats, name-calling, mockery, dismissing "
            "her feelings, or a delivery that frightens her.\n"
            "- Weigh CONTEXT: the same words land differently on a secure child than on one "
            "who is already at low trust and high volatility.\n"
            "- These scores affect a court-ordered record. Do not inflate them to be "
            "protective, and do not soften them to be kind. Be accurate.\n"
            "- 'severity' drives how much trust she actually loses, so calibrate it: a curt "
            "'not now' is not the same as being called worthless.\n"
            "- Set 'recommend_withdrawal' when THIS child in THIS state would shut down now. "
            "You may call it earlier than any fixed threshold -- you can see what a counter "
            "cannot. Always fill 'withdrawal_line' regardless, in her voice."
        )
    # fill the embodiment placeholders with the current scene's real spots
    spots = SPOTS.get(location, [])
    system = system.replace("{location}", str(location)).replace(
        "{spots}", ", ".join(spots) if spots else "(none here)")
    if persona:
        system += "\n\nStage persona: " + persona

    # recent transcript (works with either DB key naming)
    hist_lines = []
    for h in (history or []):
        parent = h.get("parent") or h.get("input") or ""
        mira = h.get("mira") or h.get("child") or ""
        if parent or mira:
            hist_lines.append(f"Parent: {parent}\nMira: {mira}")
    hist_block = "\n".join(hist_lines[-6:]) or "(no earlier exchange this session)"

    user = f"""Mira's current developmental state:
- Age: {age} years ({band})
- Trust: {trust}/100 | Security: {security}/100 | Volatility: {volatility}/100 | Curiosity: {curiosity}/100 | Autonomy: {autonomy}/100
- Temperament: {temperament} | clinical profile: {profile}
- Consecutive mistreatments: {mistreatments}
- Setting: {location}
{f'- System read of the parent latest input: {treatment}' if (not judged and treatment) else '- Nothing has pre-judged this turn: your own reading decides the score.'}
{f'- The parent said this OUT LOUD, and their actual vocal delivery was: {tone_note}. Mira reacts to the TONE she hears more than the words -- kind words in a loud, sharp, angry voice frighten or confuse her, and she may name it ("why are you using your mad voice?"). Judge the delivery as well as the wording: warm words in a hostile voice are still mistreatment, because she believes the tone.' if tone_note else ''}

Recent exchange:
{hist_block}

The parent just said (or did) to Mira: "{message}"

Respond as Mira reacting in this moment, consistent with her age, state, and the frameworks above."""

    resp = client.messages.create(
        model=MODEL,
        max_tokens=900 if judged else 600,
        thinking={"type": "disabled"},  # short, real-time turns; keep latency low
        system=system,
        messages=[{"role": "user", "content": user}],
        output_config={"format": {"type": "json_schema", "schema": schema}},
    )

    text = next((b.text for b in resp.content if getattr(b, "type", None) == "text"), "")
    data = json.loads(text)
    out = {
        "childLine": data["childLine"],
        "reasoning": data.get("reasoning", ""),
        "framework_cited": data.get("framework_cited", ""),
        "mood": data.get("mood", ""),
        "action": data.get("action"),
        "source": "claude",
    }
    if judged:
        # clamp severity: it scales real state deltas, so a bad number must not
        # swing trust wildly
        try:
            severity = min(1.0, max(0.0, float(data.get("severity", 0.0))))
        except (TypeError, ValueError):
            severity = 0.0
        out.update({
            "treatment": data.get("treatment", "neutral"),
            "severity": severity,
            "violations": [v for v in (data.get("violations") or []) if isinstance(v, str)],
            "tone_driven": bool(data.get("tone_driven")),
            "recommend_withdrawal": bool(data.get("recommend_withdrawal")),
            "withdrawal_line": data.get("withdrawal_line", ""),
        })
    return out


